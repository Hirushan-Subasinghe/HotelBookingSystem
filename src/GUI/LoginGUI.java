    private void jButton6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton6MouseClicked
        String adminUserName = jTextField3.getText();
        String adminPassword = jPasswordField1.getText();
        
        if(adminUserName.equals("") || adminPassword.equals("")){
            jTextField5.setText("All fields are required");
            Timer timer = new Timer(2000, new ActionListener() {
                    @Override
                    public void actionPerformed(ActionEvent e) {
                        // Clear the text after the delay
                        jTextField5.setText("");
                    }
                    });

                    // Start the timer
                    timer.setRepeats(false); // Set to false to execute only once
                    timer.start();
        }
        
        else{
            ResultSet rs = Select.getData("SELECT * FROM USER WHERE userName = '"+adminUserName+"' AND userPassword = '"+adminPassword+"' AND userType = 'admin' ");
            
            try{
                if(rs.next()){
                    setVisible(false);
                    new AdminGUI().setVisible(true);
                }
                
                else{
                    jTextField5.setText("Email and user name doesn't match");
                    setTextAndClearAfterDelay(jTextField5);
                }
            }
            
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        
        
    }//GEN-LAST:event_jButton6MouseClicked


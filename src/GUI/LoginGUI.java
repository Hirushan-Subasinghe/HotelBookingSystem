    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        String adminUserName = jTextField1.getText();
        String adminPassword = jPasswordField2.getText();
        
        if(adminUserName.equals("") || adminPassword.equals("")){
            jTextField6.setText("All fields are required");
            setTextAndClearAfterDelay(jTextField6);
        }
        
        else{
            
            ResultSet rs = Select.getData("SELECT * FROM USER WHERE userName = '"+adminUserName+"' AND userPassword = '"+adminPassword+"' AND userType = 'employee'");
            
            
            try{
                
                if(rs.next()){
                    String userStatus = rs.getString("userStatus");
                    
                    if(userStatus.equals("verified")){
                        setVisible(false);
                        new MainGUI().setVisible(true);
                    }
                    
                    else if(userStatus.equals("pending")){
                        jTextField6.setText("User not verified yet");
                    }
                    
                }
                                
                else{
                    jTextField6.setText("Email and user name doesn't match");
                    setTextAndClearAfterDelay(jTextField6);
                }
            }
            
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton4MouseClicked


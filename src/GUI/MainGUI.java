    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        String roomNo = jTextField2.getText();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

    try {
        if (roomNo.isEmpty()) {
            // If jTextField2 is empty, retrieve all rows
            ResultSet rsAll = Select.getData("SELECT * FROM ROOM");
            model.setRowCount(0);  // Remove existing rows

            while (rsAll.next()) {
                model.addRow(new Object[]{
                    rsAll.getString("roomNo"),
                    rsAll.getString("roomType"),
                    rsAll.getString("bedType"),
                    rsAll.getString("bookStatus"),
                    rsAll.getString("pricePerDay")
                });
            }

            rsAll.close();
        } else {
            // If jTextField2 is not empty, perform the specific query
            ResultSet rs = Select.getData("SELECT * FROM ROOM WHERE roomNo = '" + roomNo + "'");
            
            if (rs.next()) {
                model.setRowCount(0);  // Remove existing rows
                model.addRow(new Object[]{
                    rs.getString("roomNo"),
                    rs.getString("roomType"),
                    rs.getString("bedType"),
                    rs.getString("bookStatus"),
                    rs.getString("pricePerDay")
                });
            }
            
            else{
                jTextField45.setText("Enter a exist room number !");
                setTextAndClearAfterDelay(jTextField45);
            }

            rs.close();
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_jButton2MouseClicked


    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
        int index = jTable3.getSelectedRow();
        TableModel model = jTable3.getModel();
        
        String reservationID = model.getValueAt(index,0).toString();

        try{
            int a = JOptionPane.showConfirmDialog(null, "Do you want to Checkout this Reservation ","select", JOptionPane.YES_NO_OPTION);
            if(a==0){
                try{
                    String query = "UPDATE ROOM SET bookStatus= 'notBooked' WHERE roomNo IN(SELECT roomNo FROM RESERVATION WHERE reservationID = '"+reservationID+"' )";
                    InsertUpdateDelete.setData(query);
                    JOptionPane.showMessageDialog(null, "Customer details updated succesfully ! ");
                }
                
                catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
                }
                
            }
        }

        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jTable3MouseClicked

